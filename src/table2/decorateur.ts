// les decorateurs 

// Les décorateurs fournissent un mécanisme permettant d'ajouter des métadonnées, de modifier le comportement, de valider ou d'étendre les fonctionnalités de l'élément cible. Ce sont des fonctions qui s'exécutent à l'exécution. Plusieurs décorateurs peuvent être appliqués à une déclaration.

// Les décorateurs sont des fonctionnalités expérimentales et les exemples suivants ne sont compatibles qu'avec TypeScript version 5 ou supérieure utilisant ES6.

// Pour les versions TypeScript antérieures à 5, elles doivent être activées à l'aide de la experimentalDecoratorspropriété dans votre tsconfig.jsonou en utilisant --experimentalDecoratorsdans votre ligne de commande (mais l'exemple suivant ne fonctionnera pas).

// Certains des cas d’utilisation courants des décorateurs incluent :

// Observer les changements de propriété.
// Surveillance des appels de méthode.
// Ajout de propriétés ou de méthodes supplémentaires.
// Validation d'exécution.
// Sérialisation et désérialisation automatiques.
// Enregistrement.
// Autorisation et authentification.
// Protection contre les erreurs.

